#define Idle 0
#define Screen 1

enum state {
  State_Idle,
  State_Moving
};

// assuming userinput is y axis of player
void Screen1 (uint32_t userinput){
	int state = State_Idle;
  switch (state) {
    case State_Idle:
    if (userinput > 75){
      side_task(); //clouds move side to side
      state = State_Idle;
    }
    else{
      state = State_Moving;
    }
    case State_Moving:
    if (userinput <= 75){
      task_down(); // moves player and platforms down
                   // resets userinput&player(y) = 155
      state = State_Idle;
    }
    
  }
  }


	
